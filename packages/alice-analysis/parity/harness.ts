// --- Main driver -----------------------------------------------------------

async function runMatrix() {
  console.log('=== alice-analysis parity harness ===')
  console.log(`fixtures dir: ${fixturesDir}`)
  console.log(`fixtures available: ${readdirSync(fixturesDir).filter((f) => f.endsWith('.json')).join(', ')}`)
  console.log('')

  const matrix: CellResult[] = []
  let totalMsTs = 0
  let totalMsRust = 0

  for (const f of FIXTURES) {
    const fx = loadFixture(f.name)
    const ctx = fixtureContext(fx)
    const cols = fixtureColumns(fx)
    const suite = indicatorSuite(f.symbol)
    for (const { indicator, formula } of suite) {
      let result: CellResult
      try {
        // Raw f64 comparison: call the kernels directly, no precision quant.
        const tsRaw = callKernel('ts', indicator, cols)
        const rustRaw = callKernel('rust', indicator, cols)
        // Rounded comparison: full IndicatorCalculator pipeline (parser, eval,
        // js_to_fixed / parseFloat(toFixed)). At precision=4 both should land
        // on the same 4-decimal grid for any value not on a half-grid edge.
        const tsR = await runFormula('ts', ctx, formula, 4)
        const rustR = await runFormula('rust', ctx, formula, 4)
        totalMsTs += tsRaw.ms + tsR.ms
        totalMsRust += rustRaw.ms + rustR.ms
        result = compareCell(
          indicator,
          f.name,
          formula,
          { ts: tsRaw.value as number | number[] | Record<string, number>, rust: rustRaw.value as number | number[] | Record<string, number> },
          { ts: tsR.value, rust: rustR.value },
          tsRaw.ms + tsR.ms,
          rustRaw.ms + rustR.ms,
        )
      } catch (e) {
        result = {
          indicator,
          fixture: f.name,
          formula,
          pass: false,
          details: `EXCEPTION: ${(e as Error).message}`,
          raw: { ts: null, rust: null },
          rounded: { ts: null, rust: null },
          deltas: { exception: { metric: 'exact', delta: NaN, pass: false } },
          publicApiBitExact: false,
          msTs: 0,
          msRust: 0,
        }
      }
      matrix.push(result)
      const tag = result.pass ? 'PASS' : 'FAIL'
      console.log(`[${tag}] ${f.name.padEnd(18)} ${indicator.padEnd(8)} ${result.details}`)
    }
  }

  return { matrix, totalMsTs, totalMsRust }
}

