// --- Per-impl driver -------------------------------------------------------

type Impl = 'ts' | 'rust'

async function runFormula(
  impl: Impl,
  ctx: IndicatorContext,
  formula: string,
  precision = 4,
): Promise<{ value: number | number[] | Record<string, number>; ms: number }> {
  process.env.ALICE_RUST_INDICATORS = impl === 'rust' ? '*' : ''
  const calc = new IndicatorCalculator(ctx)
  const t0 = process.hrtime.bigint()
  const out = await calc.calculate(formula, precision)
  const t1 = process.hrtime.bigint()
  return { value: out.value as number | number[] | Record<string, number>, ms: Number(t1 - t0) / 1e6 }
}

// Direct raw-kernel comparison: bypasses `IndicatorCalculator`'s precision
// quantization step on both sides so we measure the kernels' f64 outputs at
// full precision. The full-pipeline comparison (with `parseFloat(toFixed(4))`
// → `js_to_fixed(...,4)`) happens separately in the rounded-API branch.
//
// Returns scalar f64 for SMA/EMA/STDEV/MAX/MIN/SUM/AVERAGE/RSI/ATR and a
// Record<field, f64> for BBANDS/MACD. Inputs come from the fixture rows.
type RawValue = number | Record<string, number>

interface KernelCfg {
  ts: (closes: number[], highs: number[], lows: number[]) => RawValue
  rust: (closes: Float64Array, highs: Float64Array, lows: Float64Array) => RawValue
}

function kernelFor(indicator: string): KernelCfg {
  switch (indicator) {
    case 'SMA':
      return {
        ts: (c) => TsStats.SMA(c, 50),
        rust: (c) => smaRaw(c, 50),
      }
    case 'EMA':
      return {
        ts: (c) => TsStats.EMA(c, 20),
        rust: (c) => emaRaw(c, 20),
      }
    case 'STDEV':
      return {
        ts: (c) => TsStats.STDEV(c),
        rust: (c) => stdevRaw(c),
      }
    case 'MAX':
      return { ts: (c) => TsStats.MAX(c), rust: (c) => maxRaw(c) }
    case 'MIN':
      return { ts: (c) => TsStats.MIN(c), rust: (c) => minRaw(c) }
    case 'SUM':
      return { ts: (c) => TsStats.SUM(c), rust: (c) => sumRaw(c) }
    case 'AVERAGE':
      return { ts: (c) => TsStats.AVERAGE(c), rust: (c) => averageRaw(c) }
    case 'RSI':
      return {
        ts: (c) => TsTech.RSI(c, 14),
        rust: (c) => rsiRaw(c, 14),
      }
    case 'BBANDS':
      return {
        ts: (c) => TsTech.BBANDS(c, 20, 2) as Record<string, number>,
        rust: (c) => bbandsRaw(c, 20, 2) as Record<string, number>,
      }
    case 'MACD':
      return {
        ts: (c) => TsTech.MACD(c, 12, 26, 9) as Record<string, number>,
        rust: (c) => macdRaw(c, 12, 26, 9) as Record<string, number>,
      }
    case 'ATR':
      return {
        ts: (_c, h, l) => TsTech.ATR(h, l, _c, 14),
        rust: (c, h, l) => atrRaw(h, l, c, 14),
      }
    default:
      throw new Error(`unknown indicator ${indicator}`)
  }
}

function fixtureColumns(fx: Fixture): {
  closes: number[]
  highs: number[]
  lows: number[]
  closesF64: Float64Array
  highsF64: Float64Array
  lowsF64: Float64Array
} {
  const n = fx.data.length
  const closes = new Array<number>(n)
  const highs = new Array<number>(n)
  const lows = new Array<number>(n)
  for (let i = 0; i < n; i++) {
    closes[i] = fx.data[i].close
    highs[i] = fx.data[i].high
    lows[i] = fx.data[i].low
  }
  return {
    closes,
    highs,
    lows,
    closesF64: Float64Array.from(closes),
    highsF64: Float64Array.from(highs),
    lowsF64: Float64Array.from(lows),
  }
}

function callKernel(impl: Impl, indicator: string, cols: ReturnType<typeof fixtureColumns>): { value: RawValue; ms: number } {
  const cfg = kernelFor(indicator)
  const t0 = process.hrtime.bigint()
  const v =
    impl === 'ts'
      ? cfg.ts(cols.closes, cols.highs, cols.lows)
      : cfg.rust(cols.closesF64, cols.highsF64, cols.lowsF64)
  const t1 = process.hrtime.bigint()
  return { value: v, ms: Number(t1 - t0) / 1e6 }
}

// --- Result rows -----------------------------------------------------------

interface CellResult {
  indicator: string
  fixture: string
  formula: string
  pass: boolean
  details: string
  raw: { ts: unknown; rust: unknown }
  rounded: { ts: unknown; rust: unknown }
  // Per-field deltas for object indicators; single-field 'value' for scalars.
  deltas: Record<string, { metric: string; delta: number; bound?: number; pass: boolean }>
  publicApiBitExact: boolean
  msTs: number
  msRust: number
}

function pickFieldTol(name: string, field: string | null): Tolerance {
  const top = TOLERANCES[name]
  if (!top) return { kind: 'releps', bound: 1e-11 }
  if (field && top.byField && top.byField[field]) return top.byField[field]
  return top
}

function compareCell(
  indicator: string,
  fixture: string,
  formula: string,
  raw: { ts: number | number[] | Record<string, number>; rust: number | number[] | Record<string, number> },
  rounded: { ts: number | number[] | Record<string, number>; rust: number | number[] | Record<string, number> },
  msTs: number,
  msRust: number,
): CellResult {
  const deltas: CellResult['deltas'] = {}
  let allOk = true

  if (typeof raw.ts === 'number' && typeof raw.rust === 'number') {
    const tol = pickFieldTol(indicator, null)
    const cmp = compareScalar(raw.ts, raw.rust, tol)
    deltas.value = { metric: cmp.metric, delta: cmp.delta, bound: tol.bound, pass: cmp.ok }
    if (!cmp.ok) allOk = false
  } else if (
    raw.ts &&
    typeof raw.ts === 'object' &&
    raw.rust &&
    typeof raw.rust === 'object' &&
    !Array.isArray(raw.ts) &&
    !Array.isArray(raw.rust)
  ) {
    const keys = Array.from(new Set([...Object.keys(raw.ts), ...Object.keys(raw.rust)]))
    for (const k of keys) {
      const a = (raw.ts as Record<string, number>)[k]
      const b = (raw.rust as Record<string, number>)[k]
      if (a === undefined || b === undefined) {
        deltas[k] = { metric: 'exact', delta: NaN, pass: false }
        allOk = false
        continue
      }
      const tol = pickFieldTol(indicator, k)
      const cmp = compareScalar(a, b, tol)
      deltas[k] = { metric: cmp.metric, delta: cmp.delta, bound: tol.bound, pass: cmp.ok }
      if (!cmp.ok) allOk = false
    }
  } else if (Array.isArray(raw.ts) && Array.isArray(raw.rust)) {
    if (raw.ts.length !== raw.rust.length) {
      deltas.length = { metric: 'exact', delta: Math.abs(raw.ts.length - raw.rust.length), pass: false }
      allOk = false
    } else {
      let maxDelta = 0
      let perField: { metric: string; delta: number; bound?: number; pass: boolean } = {
        metric: 'releps',
        delta: 0,
        bound: 1e-12,
        pass: true,
      }
      const tol = pickFieldTol(indicator, null)
      for (let i = 0; i < raw.ts.length; i++) {
        const cmp = compareScalar(raw.ts[i], raw.rust[i], tol)
        if (cmp.delta > maxDelta) {
          maxDelta = cmp.delta
          perField = { metric: cmp.metric, delta: cmp.delta, bound: tol.bound, pass: cmp.ok }
        }
        if (!cmp.ok) allOk = false
      }
      deltas.maxElement = perField
    }
  } else {
    deltas.shape = { metric: 'exact', delta: NaN, pass: false }
    allOk = false
  }

  // Public-API parity: TS and Rust outputs after `parseFloat(toFixed(4))`
  // should be bit-exact for any value not on a `xxxx.xxxx5` boundary. Use
  // value-equality (sorted key compare) — TS uses insertion order, Rust uses
  // BTreeMap (alphabetical). The numeric values are what we care about.
  const bitExact = deepValueEqual(rounded.ts, rounded.rust)

  let details: string
  if (allOk) {
    const summary = Object.entries(deltas)
      .map(([k, v]) => `${k}: ${v.metric}=${formatDelta(v.delta)}`)
      .join(', ')
    details = `PASS (${summary})`
  } else {
    const fails = Object.entries(deltas)
      .filter(([, v]) => !v.pass)
      .map(([k, v]) => `${k}: ${v.metric}=${formatDelta(v.delta)} > bound ${v.bound}`)
      .join('; ')
    details = `FAIL: ${fails}`
  }

  return {
    indicator,
    fixture,
    formula,
    pass: allOk,
    details,
    raw,
    rounded,
    deltas,
    publicApiBitExact: bitExact,
    msTs,
    msRust,
  }
}

function deepValueEqual(a: unknown, b: unknown): boolean {
  if (typeof a === 'number' && typeof b === 'number') return Object.is(a, b)
  if (Array.isArray(a) && Array.isArray(b)) {
    if (a.length !== b.length) return false
    for (let i = 0; i < a.length; i++) {
      if (!deepValueEqual(a[i], b[i])) return false
    }
    return true
  }
  if (a && b && typeof a === 'object' && typeof b === 'object') {
    const ka = Object.keys(a as Record<string, unknown>).sort()
    const kb = Object.keys(b as Record<string, unknown>).sort()
    if (ka.length !== kb.length) return false
    for (let i = 0; i < ka.length; i++) {
      if (ka[i] !== kb[i]) return false
      if (!deepValueEqual((a as Record<string, unknown>)[ka[i]], (b as Record<string, unknown>)[kb[i]])) return false
    }
    return true
  }
  return a === b
}

function formatDelta(d: number): string {
  if (!Number.isFinite(d)) return String(d)
  if (d === 0) return '0'
  if (Math.abs(d) < 1e-3) return d.toExponential(2)
  if (Number.isInteger(d)) return String(d)
  return d.toFixed(6)
}

