/**
 * Parity harness — drives both the TS and Rust impls of `IndicatorCalculator`
 * through identical call sites and compares outputs per design §5.2.
 *
 * Strategy (chosen over the file-dump-and-diff alternative the team-lead
 * sketched): a focused harness in one process that flips
 * `process.env.ALICE_RUST_INDICATORS` per call. The migration switch in
 * `src/domain/analysis/indicator/calculator.ts` reads the env var on every
 * `calculate()`, so toggling it picks the impl. This keeps the comparison
 * apples-to-apples (same fixture, same call site, same precision pipeline) and
 * sidesteps yfinance's daily drift in the e2e suite.
 *
 * Fixtures are deterministic, generated by `generate-fixtures.mjs` and stored
 * as JSON under `parity/fixtures/`.
 *
 * Run with:
 *   /opt/homebrew/bin/node node_modules/.bin/tsx packages/alice-analysis/parity/harness.ts
 *
 * Outputs:
 *   - stdout: pass/fail summary per cell + headline counts
 *   - parity/report.json: full machine-readable report consumed by 03-parity.md
 */

