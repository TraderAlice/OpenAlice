// --- Tolerance rules straight from `_rust-port/02-design.md` §5.2 ----------

interface Tolerance {
  kind: 'exact' | 'ulp' | 'releps'
  bound?: number
  // For object-valued indicators (BBANDS, MACD), each named field can pick
  // its own rule (BBANDS.middle is exact while .upper/.lower allow 4 ULP).
  byField?: Record<string, Tolerance>
}

const TOLERANCES: Record<string, Tolerance> = {
  SMA:     { kind: 'exact' },
  MAX:     { kind: 'exact' },
  MIN:     { kind: 'exact' },
  SUM:     { kind: 'exact' },
  AVERAGE: { kind: 'exact' },
  STDEV:   { kind: 'ulp', bound: 4 },
  EMA:     { kind: 'releps', bound: 1e-12 },
  RSI:     { kind: 'releps', bound: 1e-12 },
  ATR:     { kind: 'releps', bound: 1e-12 },
  BBANDS: {
    kind: 'releps',
    bound: 1e-12,
    byField: {
      middle: { kind: 'exact' },
      upper:  { kind: 'ulp', bound: 4 },
      lower:  { kind: 'ulp', bound: 4 },
    },
  },
  MACD: {
    kind: 'releps',
    bound: 1e-11,
    byField: {
      macd:      { kind: 'releps', bound: 1e-11 },
      signal:    { kind: 'releps', bound: 1e-11 },
      histogram: { kind: 'releps', bound: 1e-11 },
    },
  },
  // Composed formulas inherit the loosest bound of the indicators they touch.
  // We compute this on-the-fly below.
}

// `parseFloat(toFixed(precision))` quantizes to a 4-decimal grid, so once
// applied the public-API output should be bit-exact across impls (per §5.2).
const PUBLIC_API_TOL: Tolerance = { kind: 'exact' }

// --- ULP distance helper (per design §5.2 spec) ---------------------------

function ulpDistance(a: number, b: number): number {
  if (a === b) return 0
  if (!Number.isFinite(a) || !Number.isFinite(b)) return Number.POSITIVE_INFINITY
  // Treat the two f64s as monotonic sign-magnitude integers, mapped to a
  // total order via the standard trick (flip the sign bit for positives, all
  // bits for negatives).
  const buf = new ArrayBuffer(8)
  const dv = new DataView(buf)
  const toBits = (x: number): bigint => {
    dv.setFloat64(0, x)
    const u = dv.getBigUint64(0)
    return (u & 0x8000_0000_0000_0000n) === 0n
      ? u | 0x8000_0000_0000_0000n
      : ~u
  }
  const ba = toBits(a)
  const bb = toBits(b)
  return Number(ba > bb ? ba - bb : bb - ba)
}

function relEps(a: number, b: number): number {
  if (a === b) return 0
  const denom = Math.max(Math.abs(a), Math.abs(b))
  if (denom === 0) return 0
  return Math.abs(a - b) / denom
}

interface Cmp {
  ok: boolean
  delta: number     // relative, ULP, or 0/1 for exact mismatch
  metric: 'exact' | 'ulp' | 'releps'
}

function compareScalar(a: number, b: number, tol: Tolerance): Cmp {
  if (Number.isNaN(a) && Number.isNaN(b)) return { ok: true, delta: 0, metric: 'exact' }
  if (Number.isNaN(a) || Number.isNaN(b)) return { ok: false, delta: NaN, metric: 'exact' }
  switch (tol.kind) {
    case 'exact':
      return { ok: Object.is(a, b), delta: a === b ? 0 : 1, metric: 'exact' }
    case 'ulp': {
      const d = ulpDistance(a, b)
      return { ok: d <= (tol.bound ?? 0), delta: d, metric: 'ulp' }
    }
    case 'releps': {
      const d = relEps(a, b)
      return { ok: d <= (tol.bound ?? 0), delta: d, metric: 'releps' }
    }
  }
}

